from time import sleep
from typing import Type, Tuple

from act import type, val


type Void = None | Disturbance
type Nothing = Void | Space | SpaceTime

Disturbance = type(a=Void, b=Void)

Space: Type[Tuple[Void]] = tuple
SpaceTime: Type[list[Space]] = list


@val
class propagated:
    def __call__(nothing: Nothing = None) -> Nothing:
        if nothing is None:
            return Disturbance(nothing, nothing)

        elif isinstance(nothing, Disturbance):
            return Space([None, nothing])

        elif isinstance(nothing, Space):
            return SpaceTime([nothing])

        elif isinstance(nothing, SpaceTime):
            past = nothing[:-1]
            present = nothing[-1]
            future = propagated._future_of(present)

            return SpaceTime([*past, present, future])

    def _future_of(space: Space) -> Space:
        space = Space(
            Disturbance(first_void, second_void)
            for first_void in space
            for second_void in space
        )

        return Space([None, *space])


def repr_of(nothing: Nothing) -> str:
    if nothing is None:
        return 'v'

    if isinstance(nothing, Disturbance):
        return f"[{repr_of(nothing.a)} -> {repr_of(nothing.b)}]"

    if isinstance(nothing, Space):
        return f"Space({"; ".join(map(repr_of, nothing))})"

    if isinstance(nothing, SpaceTime):
        return f"SpaceTime({" ---> ".join(map(repr_of, nothing))})"


def show(nothing: Nothing) -> None:
    print(repr_of(nothing), "\n")
    sleep(2)


def main() -> None:
    nothing = None

    while True:
        show(nothing)
        nothing = propagated(nothing)


if __name__ == "__main__":
    main()

